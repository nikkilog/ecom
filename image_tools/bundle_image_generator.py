def make_placement(view: str, x: float, y: float, w: float, h: float, scale: float, fit: str) -> Placement:
    return Placement(view=view, x=x, y=y, w=w, h=h, scale=scale, fit=fit)


def placements_matrix(rule: LayoutRule) -> List[Placement]:
    if rule.count <= 0:
        return []

    width = rule.x2 - rule.x1
    height = rule.y2 - rule.y1
    cell_w = width / rule.cols
    cell_h = height / rule.rows

    placements: List[Placement] = []
    for i in range(rule.count):
        r = i // rule.cols
        c = i % rule.cols
        if r >= rule.rows:
            break
        x = rule.x1 + cell_w * (c + 0.5)
        y = rule.y1 + cell_h * (r + 0.5)
        placements.append(
            make_placement(
                view=rule.view,
                x=x,
                y=y,
                w=cell_w,
                h=cell_h,
                scale=rule.scale,
                fit=rule.fit,
            )
        )
    return placements


def placements_center(rule: LayoutRule) -> List[Placement]:
    x = (rule.x1 + rule.x2) / 2
    y = (rule.y1 + rule.y2) / 2
    w = rule.x2 - rule.x1
    h = rule.y2 - rule.y1
    return [make_placement(rule.view, x, y, w, h, rule.scale, rule.fit) for _ in range(max(1, rule.count))]


def placements_vertical(rule: LayoutRule) -> List[Placement]:
    # Vertical means count items top-to-bottom in one column unless rows/cols explicitly define another matrix.
    cols = max(1, rule.cols)
    rows = max(rule.rows, (rule.count + cols - 1) // cols)
    proxy = LayoutRule(**{**rule.__dict__, "rows": rows, "cols": cols})
    return placements_matrix(proxy)


def placements_horizontal(rule: LayoutRule) -> List[Placement]:
    # Horizontal means count items left-to-right in one row unless rows/cols explicitly define another matrix.
    rows = max(1, rule.rows)
    cols = max(rule.cols, (rule.count + rows - 1) // rows)
    proxy = LayoutRule(**{**rule.__dict__, "rows": rows, "cols": cols})
    return placements_matrix(proxy)


def placements_diagonal(rule: LayoutRule) -> List[Placement]:
    if rule.count <= 0:
        return []

    # If only one item, place it slightly according to role implication but still centered in its box.
    if rule.count == 1:
        return placements_center(rule)

    width = rule.x2 - rule.x1
    height = rule.y2 - rule.y1
    w = width / max(1.25, min(2.0, rule.count))
    h = height / max(1.25, min(2.0, rule.count))

    placements: List[Placement] = []
    for i in range(rule.count):
        t = i / max(1, rule.count - 1)
        x = rule.x1 + width * (0.30 + 0.40 * t)
        y = rule.y1 + height * (0.30 + 0.40 * t)
        placements.append(make_placement(rule.view, x, y, w, h, rule.scale, rule.fit))
    return placements


def placements_pyramid(rule: LayoutRule) -> List[Placement]:
    if rule.count <= 0:
        return []

    # Supported practical patterns:
    # 3 -> 1 + 2
    # 5 -> 1 + 2 + 2
    # Otherwise use matrix fallback.
    width = rule.x2 - rule.x1
    height = rule.y2 - rule.y1

    if rule.count == 3:
        points = [
            (0.50, 0.28),
            (0.34, 0.68),
            (0.66, 0.68),
        ]
        w = width * 0.46
        h = height * 0.38
    elif rule.count == 5:
        points = [
            (0.50, 0.18),
            (0.34, 0.48),
            (0.66, 0.48),
            (0.34, 0.78),
            (0.66, 0.78),
        ]
        w = width * 0.42
        h = height * 0.30
    else:
        return placements_matrix(rule)

    return [
        make_placement(
            rule.view,
            rule.x1 + width * px,
            rule.y1 + height * py,
            w,
            h,
            rule.scale,
            rule.fit,
        )
        for px, py in points[: rule.count]
    ]


def placements_for_rule(rule: LayoutRule) -> List[Placement]:
    pattern = rule.pattern
    if pattern == "center" or pattern == "manual":
        return placements_center(rule)
    if pattern == "matrix":
        return placements_matrix(rule)
    if pattern == "vertical":
        return placements_vertical(rule)
    if pattern == "horizontal":
        return placements_horizontal(rule)
    if pattern == "diagonal":
        return placements_diagonal(rule)
    if pattern == "pyramid":
        return placements_pyramid(rule)
    raise ValueError(f"Unsupported pattern: {pattern}")


