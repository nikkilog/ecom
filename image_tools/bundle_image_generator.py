def normalize_input_df(input_df: pd.DataFrame) -> pd.DataFrame:
    df = input_df.copy()
    df.columns = [normalize_header(c) for c in df.columns]
    validate_columns(df, REQUIRED_INPUT_COLUMNS, "Input__BundleImageURLs")

    for c in REQUIRED_INPUT_COLUMNS:
        df[c] = df[c].apply(clean_cell)

    df["Type"] = df["Type"].apply(normalize_template_key)
    df["template_no"] = df["template_no"].apply(normalize_template_key)

    if "enabled" in df.columns:
        df["enabled"] = df["enabled"].apply(lambda x: clean_cell(x).lower())
        df = df[df["enabled"].isin(["", "true", "yes", "y", "1", "on", "enabled"])].copy()

    df = df[df["sku"].astype(str).str.strip() != ""].copy()
    return df


def normalize_template_df(template_df: pd.DataFrame) -> pd.DataFrame:
    df = template_df.copy()
    df.columns = [normalize_header(c) for c in df.columns]
    validate_columns(df, REQUIRED_TEMPLATE_COLUMNS, "Ref__BundleTemplates")

    for c in REQUIRED_TEMPLATE_COLUMNS:
        df[c] = df[c].apply(clean_cell)

    df["template_no"] = df["template_no"].apply(normalize_template_key)

    for c in OPTIONAL_TEMPLATE_COLUMNS:
        if c in df.columns:
            df[c] = df[c].apply(clean_cell)

    # Drop accidental repeated header rows.
    df = df[
        ~(
            (df["template_no"].str.lower() == "template_no")
            | (df["template_name"].str.lower() == "template_name")
        )
    ].copy()

    df = df[df["template_name"].astype(str).str.strip() != ""].copy()
    return df


def normalize_layout_df(layout_df: pd.DataFrame) -> pd.DataFrame:
    df = layout_df.copy()
    df.columns = [normalize_header(c) for c in df.columns]
    validate_columns(df, REQUIRED_LAYOUT_COLUMNS, "Ref__BundleTemplateLayouts")

    for c in REQUIRED_LAYOUT_COLUMNS:
        df[c] = df[c].apply(clean_cell)

    if "enabled" not in df.columns:
        df["enabled"] = "TRUE"
    else:
        df["enabled"] = df["enabled"].apply(clean_cell)

    if "fit" not in df.columns:
        df["fit"] = "contain"
    else:
        df["fit"] = df["fit"].apply(clean_cell)
        df.loc[df["fit"] == "", "fit"] = "contain"

    # Drop accidental repeated header rows pasted into the body.
    df = df[
        ~(
            (df["template_name"].str.lower() == "template_name")
            | (df["quantity"].str.lower() == "quantity")
            | (df["role"].str.lower() == "role")
        )
    ].copy()

    # Drop blank rows.
    df = df[df["template_name"].astype(str).str.strip() != ""].copy()
    df = df[df["quantity"].astype(str).str.strip() != ""].copy()

    # Enabled filter.
    df = df[df["enabled"].apply(lambda x: is_true_like(x, default=True))].copy()

    df["quantity"] = df["quantity"].apply(lambda x: to_int(x, "quantity"))
    df["count"] = df["count"].apply(lambda x: to_int(x, "count", default=1))
    df["rows"] = df["rows"].apply(lambda x: to_int(x, "rows", default=1))
    df["cols"] = df["cols"].apply(lambda x: to_int(x, "cols", default=1))
    df["z_index"] = df["z_index"].apply(lambda x: to_int(x, "z_index", default=1))

    for c in ["x1", "y1", "x2", "y2"]:
        df[c] = df[c].apply(lambda x: to_float(x, c))
    df["scale"] = df["scale"].apply(lambda x: to_float(x, "scale", default=1.0))

    df["template_name"] = df["template_name"].apply(clean_cell)
    df["role"] = df["role"].apply(lambda x: clean_cell(x).lower())
    df["pattern"] = df["pattern"].apply(lambda x: clean_cell(x).lower() or "matrix")
    df["view"] = df["view"].apply(lambda x: clean_cell(x).lower() or "image")
    df["fit"] = df["fit"].apply(lambda x: clean_cell(x).lower() or "contain")

    for _, row in df.iterrows():
        context = f"template={row['template_name']}, quantity={row['quantity']}, role={row['role']}"
        validate_box(row["x1"], row["y1"], row["x2"], row["y2"], context)
        if row["count"] < 0:
            raise ValueError(f"count must be >= 0: {context}")
        if row["rows"] <= 0 or row["cols"] <= 0:
            raise ValueError(f"rows/cols must be positive: {context}")
        if row["view"] not in VIEW_KEYS:
            # Do not fail; view fallback happens later, but surface bad config strongly.
            raise ValueError(f"Unsupported view={row['view']} in {context}; supported: {VIEW_KEYS}")
        if row["pattern"] not in SUPPORTED_PATTERNS:
            raise ValueError(f"Unsupported pattern={row['pattern']} in {context}; supported: {sorted(SUPPORTED_PATTERNS)}")
        if row["fit"] not in {"contain", "cover"}:
            raise ValueError(f"Unsupported fit={row['fit']} in {context}; supported: contain/cover")

    return df


def build_layout_map(layout_df: pd.DataFrame) -> Dict[Tuple[str, int], List[LayoutRule]]:
    df = normalize_layout_df(layout_df)
    layout_map: Dict[Tuple[str, int], List[LayoutRule]] = {}

    for _, row in df.iterrows():
        rule = LayoutRule(
            template_name=clean_cell(row["template_name"]),
            quantity=int(row["quantity"]),
            role=clean_cell(row["role"]).lower(),
            pattern=clean_cell(row["pattern"]).lower(),
            view=clean_cell(row["view"]).lower(),
            count=int(row["count"]),
            rows=int(row["rows"]),
            cols=int(row["cols"]),
            x1=float(row["x1"]),
            y1=float(row["y1"]),
            x2=float(row["x2"]),
            y2=float(row["y2"]),
            scale=float(row["scale"]),
            z_index=int(row["z_index"]),
            fit=clean_cell(row.get("fit", "contain")).lower() or "contain",
            note=clean_cell(row.get("note", "")),
        )
        layout_map.setdefault((rule.template_name, rule.quantity), []).append(rule)

    for k in list(layout_map.keys()):
        layout_map[k] = sorted(layout_map[k], key=lambda r: r.z_index)

    return layout_map


