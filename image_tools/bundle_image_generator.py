def generate_from_dataframes(
    input_df: pd.DataFrame,
    template_df: pd.DataFrame,
    layout_df: pd.DataFrame,
    output_dir: str | Path,
    cache_dir: str | Path,
    sprite_dir: Optional[str | Path] = None,
    save_sprite_images: bool = False,
    quantities: Optional[List[int]] = None,
    canvas_size: int = 1500,
    output_format: str = "jpg",
    overwrite: bool = True,
    remove_bg: bool = True,
    white_threshold: int = 246,
    scale_config: Optional[Dict[str, Any]] = None,
) -> pd.DataFrame:
    input_df = normalize_input_df(input_df)
    template_df = normalize_template_df(template_df)

    output_dir = ensure_dir(output_dir)
    cache_dir = ensure_dir(cache_dir)
    sprite_dir_path = ensure_dir(sprite_dir) if sprite_dir else None

    layout_map = build_layout_map(layout_df)
    template_names_in_ref, template_no_to_name = build_template_ref_maps(template_df)

    runlog: List[Dict[str, Any]] = []

    for idx, row in input_df.iterrows():
        sku = clean_cell(row.get("sku", ""))
        output_name = clean_cell(row.get("output_name", "")) or f"{sku}-SPU"
        type_value = clean_cell(row.get("Type", ""))
        raw_template_no = clean_cell(row.get("template_no", ""))
        raw_template_name = clean_cell(row.get("template_name", ""))

        resolved_template_name = raw_template_name
        template_resolution_note = ""
        candidate_quantities: List[int] = []
        actual_quantities: List[int] = []

        started = time.time()

        try:
            resolved_template_name, resolved_template_no, template_resolution_note = resolve_template_name(
                row=row,
                template_names_in_ref=template_names_in_ref,
                template_no_to_name=template_no_to_name,
            )

            candidate_quantities = resolve_candidate_quantities_by_type(type_value, quantities=quantities)
            actual_quantities = [
                q for q in candidate_quantities if (resolved_template_name, q) in layout_map
            ]
            skipped_quantities = [
                q for q in candidate_quantities if (resolved_template_name, q) not in layout_map
            ]

            if not actual_quantities:
                raise ValueError(
                    f"No matching layout quantities for template={resolved_template_name}. "
                    f"Candidate quantities={candidate_quantities}. "
                    f"Check Ref__BundleTemplateLayouts."
                )

            url_map = resolve_url_map(row)
            view_images, source_paths, sprite_paths = build_view_images(
                url_map=url_map,
                cache_dir=cache_dir,
                remove_bg=remove_bg,
                white_threshold=white_threshold,
                scale_config=scale_config,
                sku=sku,
                sprite_dir=sprite_dir_path,
                save_sprite_images=save_sprite_images,
            )

            unique_source_urls = set(url_map.values())
            source_count = len(unique_source_urls)

            # Log skipped missing layout quantities, but do not fail.
            for qty in skipped_quantities:
                runlog.append({
                    "ts": pd.Timestamp.utcnow().isoformat(),
                    "sku": sku,
                    "Type": type_value,
                    "output_name": output_name,
                    "template_no_input": raw_template_no,
                    "template_name_input": raw_template_name,
                    "template_name": resolved_template_name,
                    "template_resolution_note": template_resolution_note,
                    "quantity": qty,
                    "status": "skipped_no_layout",
                    "output_path": "",
                    "source_count": source_count,
                    "image_url": url_map.get("image", ""),
                    "front_url": url_map.get("front", ""),
                    "angle_url": url_map.get("angle", ""),
                    "side_url": url_map.get("side", ""),
                    "sprite_image_path": sprite_paths.get("image", ""),
                    "sprite_front_path": sprite_paths.get("front", ""),
                    "sprite_angle_path": sprite_paths.get("angle", ""),
                    "sprite_side_path": sprite_paths.get("side", ""),
                    "message": f"No layout for template={resolved_template_name}, quantity={qty}",
                    "elapsed_sec": round(time.time() - started, 2),
                })

            for qty in actual_quantities:
                out_ext = "jpg" if output_format.lower() in ["jpg", "jpeg"] else "png"
                filename = f"{safe_filename(output_name)}_{qty}pcs.{out_ext}"
                out_path = output_dir / filename

                if out_path.exists() and not overwrite:
                    status = "skipped_exists"
                else:
                    img = render_quantity_image(
                        view_images=view_images,
                        layout_rules=layout_map[(resolved_template_name, qty)],
                        canvas_size=canvas_size,
                        scale_config=scale_config,
                        template_name=resolved_template_name,
                    )
                    if out_ext == "jpg":
                        img.save(out_path, quality=95, optimize=True)
                    else:
                        img.save(out_path)
                    status = "success"

                runlog.append({
                    "ts": pd.Timestamp.utcnow().isoformat(),
                    "sku": sku,
                    "Type": type_value,
                    "output_name": output_name,
                    "template_no_input": raw_template_no,
                    "template_name_input": raw_template_name,
                    "template_name": resolved_template_name,
                    "template_resolution_note": template_resolution_note,
                    "quantity": qty,
                    "status": status,
                    "output_path": str(out_path),
                    "source_count": source_count,
                    "image_url": url_map.get("image", ""),
                    "front_url": url_map.get("front", ""),
                    "angle_url": url_map.get("angle", ""),
                    "side_url": url_map.get("side", ""),
                    "sprite_image_path": sprite_paths.get("image", ""),
                    "sprite_front_path": sprite_paths.get("front", ""),
                    "sprite_angle_path": sprite_paths.get("angle", ""),
                    "sprite_side_path": sprite_paths.get("side", ""),
                    "message": "",
                    "elapsed_sec": round(time.time() - started, 2),
                })

        except Exception as e:
            runlog.append({
                "ts": pd.Timestamp.utcnow().isoformat(),
                "sku": sku,
                "Type": type_value,
                "output_name": output_name,
                "template_no_input": raw_template_no,
                "template_name_input": raw_template_name,
                "template_name": resolved_template_name,
                "template_resolution_note": template_resolution_note,
                "quantity": ",".join(str(x) for x in candidate_quantities) if candidate_quantities else "",
                "status": "error",
                "output_path": "",
                "source_count": "",
                "image_url": clean_cell(row.get("image_url", "")),
                "front_url": clean_cell(row.get("front_url", "")),
                "angle_url": clean_cell(row.get("angle_url", "")),
                "side_url": clean_cell(row.get("side_url", "")),
                "sprite_image_path": "",
                "sprite_front_path": "",
                "sprite_angle_path": "",
                "sprite_side_path": "",
                "message": str(e),
                "elapsed_sec": round(time.time() - started, 2),
            })

    log_df = pd.DataFrame(runlog)
    log_path = output_dir / "bundle_image_runlog.csv"
    log_df.to_csv(log_path, index=False)

    return log_df


