def render_quantity_image(
    view_images: Dict[str, Image.Image],
    layout_rules: List[LayoutRule],
    canvas_size: int = 1500,
    background_rgb: Tuple[int, int, int] = (255, 255, 255),
    scale_config: Optional[Dict[str, Any]] = None,
    template_name: str = "",
) -> Image.Image:
    canvas = Image.new("RGBA", (canvas_size, canvas_size), background_rgb + (255,))

    render_scale = float(get_global_scale_value(scale_config, "render_scale", 1.0))
    template_scale = get_template_scale(template_name, scale_config) if template_name else 1.0

    placements: List[Placement] = []
    for rule in sorted(layout_rules, key=lambda r: r.z_index):
        placements.extend(placements_for_rule(rule))

    for item in placements:
        view = item.view
        if view not in view_images:
            view = "image"

        src = view_images[view]

        target_w = max(1, int(canvas_size * item.w * item.scale * render_scale * template_scale))
        target_h = max(1, int(canvas_size * item.h * item.scale * render_scale * template_scale))

        fitted = fit_image(src, target_w, target_h, fit=item.fit)

        cx = int(canvas_size * item.x)
        cy = int(canvas_size * item.y)
        paste_center(canvas, fitted, cx, cy)

    return canvas.convert("RGB")


